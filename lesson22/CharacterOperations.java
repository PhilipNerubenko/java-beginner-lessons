package lesson22;

/**
* A class that demonstrates character operations in Java.
*/
public class CharacterOperations {
 /**
  * A method that returns null.
  *
  * @return null
  */
 String abc() {
   return null;
 }

 /**
  * A method that returns the ASCII value of the character 'h'.
  *
  * @return The ASCII value of the character 'h'.
  */
 int abc2() {
   char c = 'h';
   return c;
 }
}
